class Point:
    def __init__(self, x, y, p, q):
        self.x = x
        self.y = y
        self.p = p
        self.q = q

    def getX(self):
        return  self.x

    def getY(self):
        return  self.y

    def getP(self):
        return  self.p

    def getQ(self):
        return  self.q

